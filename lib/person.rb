class Person
  #getter
  #getter and setter methods are how to fill out the objects in ruby
  #make it force of habit to automatically fill in the end when doing anything
  def name=(name)
    @name=name
  end
  #setter
  def name
    @name
  end
  #getter
  def job=(job)
    @job=job
  end
  #setter
  def job
    @job
  end
end